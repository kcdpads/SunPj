package finalWeb.command;

public class MainCommand {
	
	private String designer;
	private String stylePhoto;
	private int price;
	private String styleName;
	private String sliderImage;
	private String sliderVar;
	
	
	public String getSliderVar() {
		return sliderVar;
	}
	public void setSliderVar(String sliderVar) {
		this.sliderVar = sliderVar;
	}
	public String getSliderImage() {
		return sliderImage;
	}
	public void setSliderImage(String sliderImage) {
		this.sliderImage = sliderImage;
	}
	public String getStylePhoto() {
		return stylePhoto;
	}
	public void setStylePhoto(String stylePhoto) {
		this.stylePhoto = stylePhoto;
	}
	public String getStyleName() {
		return styleName;
	}
	public void setStyleName(String styleName) {
		this.styleName = styleName;
	}
	public String getDesigner() {
		return designer;
	}
	public void setDesigner(String designer) {
		this.designer = designer;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}

}
