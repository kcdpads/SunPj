package finalWeb.command;

//사진테이블
public class PictureCommand {

	private String photoPath;
	private String videoPath;
	private String group;
	private String photoVideoName;
	
	
	public String getPhotoPath() {
		return photoPath;
	}
	public void setPhotoPath(String photoPath) {
		this.photoPath = photoPath;
	}
	public String getVideoPath() {
		return videoPath;
	}
	public void setVideoPath(String videoPath) {
		this.videoPath = videoPath;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public String getPhotoVideoName() {
		return photoVideoName;
	}
	public void setPhotoVideoName(String photoVideoName) {
		this.photoVideoName = photoVideoName;
	}
	
	
}
